package ejerciciosiniciales;
public class Ejercicio5 {

    public static void main(String[] args) {
        int num=1;
        while(num<=10){
            System.out.println(num);
            num++;
        }
        System.out.println("----------------------------");
        num=1;
        do{
            System.out.println(num);
            num++;
        }while(num<=10);
        System.out.println("----------------------------");
        for(int x=1; x<=10; x++){
            System.out.println(x);
        }
    }
}
